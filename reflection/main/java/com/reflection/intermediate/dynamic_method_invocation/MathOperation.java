package com.reflection.intermediate.dynamic_method_invocation;

public class MathOperation {
	//mulitply method
	public static int mutiply(int a ,int b) {
		return a*b;
	}
	
	//addion metho
	public static int add(int a,int b) {
		return a+b;
	}
	//subtraction method
	public static int subtraction(int a,int b) {
		return a-b;
	}
}
